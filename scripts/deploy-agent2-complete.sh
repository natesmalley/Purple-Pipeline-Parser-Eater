#!/bin/bash
################################################################################
# REMEDIATION AGENT 2 - Complete Deployment Orchestration
# Deploy all 8 tasks for FedRAMP High Compliance
#
# This script orchestrates the complete deployment of:
# 1. Audit Logging Architecture Review
# 2. CloudTrail with multi-region logging
# 3. AWS Config with compliance rules
# 4. GuardDuty threat detection
# 5. VPC Flow Logs
# 6. EventBridge alerting
# 7. Comprehensive verification
# 8. Final validation & sign-off
#
# Prerequisites:
#   - AWS credentials configured
#   - terraform >= 1.0 installed
#   - aws-cli >= 2.0 installed
#   - terraform init already executed
#
# Usage:
#   chmod +x deploy-agent2-complete.sh
#   ./deploy-agent2-complete.sh [--task 2-8] [--skip-verify]
#
# Estimated Time: 45-60 minutes
#
# Generated by REMEDIATION-AGENT-2
################################################################################

set -e

# Color codes
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
MAGENTA='\033[0;35m'
CYAN='\033[0;36m'
NC='\033[0m'

# Configuration
SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"
PROJECT_ROOT="$(dirname "$SCRIPT_DIR")"
TF_DIR="$PROJECT_ROOT/deployment/aws/terraform"
DOCS_DIR="$PROJECT_ROOT/docs"

# Defaults
SKIP_VERIFY=false
TASKS_TO_RUN="2 3 4 5 6 7 8"

# Logging functions
log_header() {
    echo ""
    echo -e "${MAGENTA}╔════════════════════════════════════════════════════════╗${NC}"
    echo -e "${MAGENTA}║ $1${NC}"
    echo -e "${MAGENTA}╚════════════════════════════════════════════════════════╝${NC}"
    echo ""
}

log_task() {
    echo -e "${CYAN}─────────────────────────────────────────────────────────${NC}"
    echo -e "${CYAN}TASK $1: $2${NC}"
    echo -e "${CYAN}─────────────────────────────────────────────────────────${NC}"
}

log_info() {
    echo -e "${BLUE}[INFO]${NC} $1"
}

log_success() {
    echo -e "${GREEN}[✓]${NC} $1"
}

log_warning() {
    echo -e "${YELLOW}[!]${NC} $1"
}

log_error() {
    echo -e "${RED}[✗]${NC} $1"
    exit 1
}

# Parse command line arguments
while [[ $# -gt 0 ]]; do
    case $1 in
        --task)
            TASKS_TO_RUN="$2"
            shift 2
            ;;
        --skip-verify)
            SKIP_VERIFY=true
            shift
            ;;
        --help)
            echo "Usage: $0 [OPTIONS]"
            echo ""
            echo "Options:"
            echo "  --task TASKS        Run specific tasks (e.g., '2-6' or '2,3,4')"
            echo "  --skip-verify       Skip verification steps"
            echo "  --help              Show this help message"
            exit 0
            ;;
        *)
            echo "Unknown option: $1"
            exit 1
            ;;
    esac
done

# Verify prerequisites
verify_prerequisites() {
    log_info "Verifying prerequisites..."

    if ! command -v terraform &> /dev/null; then
        log_error "terraform not found. Install with: brew install terraform"
    fi
    log_success "terraform found"

    if ! command -v aws &> /dev/null; then
        log_error "aws CLI not found. Install from: https://aws.amazon.com/cli/"
    fi
    log_success "aws CLI found"

    if ! command -v jq &> /dev/null; then
        log_warning "jq not found. Some features will be limited. Install with: brew install jq"
    else
        log_success "jq found"
    fi

    # Verify AWS credentials
    if ! aws sts get-caller-identity &> /dev/null; then
        log_error "AWS credentials not configured. Run: aws configure"
    fi
    log_success "AWS credentials verified"

    # Verify terraform directory
    if [ ! -d "$TF_DIR" ]; then
        log_error "Terraform directory not found: $TF_DIR"
    fi
    log_success "Terraform directory found"

    # Verify terraform.tfvars exists
    if [ ! -f "$TF_DIR/terraform.tfvars" ]; then
        log_error "terraform.tfvars not found. Run: cp terraform.tfvars.example terraform.tfvars"
    fi
    log_success "terraform.tfvars found"
}

# Task 1: Architecture Review (already completed)
task_1_architecture_review() {
    log_task "1" "Review Audit Logging Architecture & Requirements"

    if [ -f "$DOCS_DIR/AGENT-2-TASK-1-ARCHITECTURE-REVIEW.md" ]; then
        log_success "Architecture review document already created"
        log_info "Location: $DOCS_DIR/AGENT-2-TASK-1-ARCHITECTURE-REVIEW.md"
    else
        log_warning "Architecture review document not found"
    fi
}

# Task 2: Deploy CloudTrail
task_2_cloudtrail() {
    log_task "2" "Deploy CloudTrail with Multi-Region Logging & Immutable Storage"

    cd "$TF_DIR"

    log_info "Step 1: Planning CloudTrail resources..."
    terraform plan \
        -target=aws_kms_key.cloudtrail \
        -target=aws_s3_bucket.cloudtrail_logs \
        -target=aws_s3_bucket_versioning.cloudtrail_logs \
        -target=aws_s3_bucket_server_side_encryption_configuration.cloudtrail_logs \
        -target=aws_s3_bucket_public_access_block.cloudtrail_logs \
        -target=aws_s3_bucket_policy.cloudtrail_allow_write \
        -target=aws_cloudtrail.main \
        -out=cloudtrail.tfplan > /dev/null
    log_success "Plan created"

    log_info "Step 2: Deploying KMS key..."
    terraform apply -auto-approve cloudtrail.tfplan > /dev/null
    KMS_KEY=$(terraform output -raw kms_cloudtrail_key_id 2>/dev/null || echo "unknown")
    log_success "KMS key deployed: $KMS_KEY"

    log_info "Step 3: Verifying CloudTrail..."
    TRAIL=$(terraform output -raw cloudtrail_name 2>/dev/null || echo "unknown")
    BUCKET=$(terraform output -raw cloudtrail_logs_bucket 2>/dev/null || echo "unknown")

    LOGGING=$(aws cloudtrail get-trail-status --name "$TRAIL" --query 'IsLogging' --output text)
    if [ "$LOGGING" = "True" ]; then
        log_success "CloudTrail is actively logging"
    else
        log_warning "CloudTrail logging status: $LOGGING"
    fi
}

# Task 3: Deploy AWS Config
task_3_config() {
    log_task "3" "Deploy AWS Config for Continuous Compliance Monitoring"

    cd "$TF_DIR"

    log_info "Deploying AWS Config resources..."
    terraform apply \
        -target=aws_config_configuration_recorder.main \
        -target=aws_config_delivery_channel.main \
        -target=aws_config_config_rule.required_tags \
        -target=aws_config_config_rule.encrypted_volumes \
        -target=aws_config_config_rule.root_account_mfa \
        -auto-approve > /dev/null

    RECORDER=$(terraform output -raw config_recorder_name 2>/dev/null || echo "unknown")
    log_success "AWS Config deployed: $RECORDER"

    # Wait for rules to evaluate
    log_info "Waiting for compliance rules to evaluate (5-10 minutes)..."
    log_info "Check status with: aws configservice describe-compliance-by-config-rule"
}

# Task 4: Enable GuardDuty
task_4_guardduty() {
    log_task "4" "Enable GuardDuty for Threat Detection"

    cd "$TF_DIR"

    log_info "Deploying GuardDuty resources..."
    terraform apply \
        -target=aws_guardduty_detector.main \
        -target=aws_cloudwatch_event_rule.guardduty_findings \
        -target=aws_sns_topic.guardduty_alerts \
        -target=aws_cloudwatch_event_target.guardduty_findings \
        -auto-approve > /dev/null

    DETECTOR=$(terraform output -raw guardduty_detector_id 2>/dev/null || echo "unknown")
    log_success "GuardDuty deployed: $DETECTOR"

    log_info "GuardDuty findings will appear after 10-30 minutes of activity"
}

# Task 5: Deploy VPC Flow Logs
task_5_vpc_flow_logs() {
    log_task "5" "Deploy VPC Flow Logs for Network Monitoring"

    cd "$TF_DIR"

    log_info "Deploying VPC Flow Logs..."
    terraform apply \
        -target=aws_iam_role.vpc_flow_logs \
        -target=aws_iam_role_policy.vpc_flow_logs \
        -target=aws_cloudwatch_log_group.vpc_flow_logs \
        -target=aws_flow_log.vpc \
        -auto-approve > /dev/null

    FLOW_LOG=$(terraform output -raw vpc_flow_logs_id 2>/dev/null || echo "unknown")
    log_success "VPC Flow Logs deployed: $FLOW_LOG"

    log_info "VPC Flow Logs events will appear after 10-15 minutes"
}

# Task 6: Configure EventBridge Alerting
task_6_eventbridge() {
    log_task "6" "Configure EventBridge Alerting & Integration"

    cd "$TF_DIR"

    log_info "Deploying EventBridge and SNS topics..."
    terraform apply \
        -target=aws_sns_topic.security_alerts \
        -target=aws_sns_topic_policy.security_alerts \
        -target=aws_cloudwatch_event_rule.cloudtrail_changes \
        -target=aws_cloudwatch_event_target.cloudtrail_changes_sns \
        -target=aws_cloudwatch_event_rule.config_compliance_change \
        -target=aws_cloudwatch_event_target.config_compliance_sns \
        -auto-approve > /dev/null

    SECURITY_TOPIC=$(terraform output -raw security_alerts_topic_arn 2>/dev/null || echo "unknown")
    log_success "EventBridge and alerting deployed"
    log_info "SNS Topic: $SECURITY_TOPIC"
}

# Task 7: Verification
task_7_verification() {
    log_task "7" "Comprehensive Verification & Audit Trail Testing"

    if [ "$SKIP_VERIFY" = true ]; then
        log_warning "Verification skipped (--skip-verify)"
        return
    fi

    cd "$TF_DIR"

    log_info "Running verification checks..."

    # Verify CloudTrail
    TRAIL=$(terraform output -raw cloudtrail_name 2>/dev/null || echo "unknown")
    LOGGING=$(aws cloudtrail get-trail-status --name "$TRAIL" --query 'IsLogging' --output text 2>/dev/null || echo "unknown")
    if [ "$LOGGING" = "True" ]; then
        log_success "CloudTrail: LOGGING"
    else
        log_warning "CloudTrail: NOT LOGGING"
    fi

    # Verify AWS Config
    RECORDER=$(terraform output -raw config_recorder_name 2>/dev/null || echo "unknown")
    RECORDING=$(aws configservice describe-configuration-recorders --configuration-recorder-names "$RECORDER" --query 'ConfigurationRecorders[0].recording' --output text 2>/dev/null || echo "unknown")
    if [ "$RECORDING" = "True" ]; then
        log_success "AWS Config: RECORDING"
    else
        log_warning "AWS Config: NOT RECORDING"
    fi

    # Verify GuardDuty
    DETECTOR=$(terraform output -raw guardduty_detector_id 2>/dev/null || echo "unknown")
    DETECTOR_STATUS=$(aws guardduty get-detector --detector-id "$DETECTOR" --query 'Detector.Status' --output text 2>/dev/null || echo "unknown")
    if [ "$DETECTOR_STATUS" = "ENABLED" ]; then
        log_success "GuardDuty: ENABLED"
    else
        log_warning "GuardDuty: $DETECTOR_STATUS"
    fi

    log_success "Verification complete"
}

