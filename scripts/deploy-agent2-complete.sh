# Main execution
main() {
    log_header "REMEDIATION AGENT 2 - FedRAMP High Compliance Deployment"

    log_info "Verifying prerequisites..."
    verify_prerequisites
    log_success "All prerequisites verified"

    # Get AWS account info
    ACCOUNT=$(aws sts get-caller-identity --query Account --output text)
    REGION=$(aws configure get region)
    log_info "AWS Account: $ACCOUNT"
    log_info "AWS Region: $REGION"

    # Run requested tasks
    if [[ "$TASKS_TO_RUN" == *"1"* ]]; then
        task_1_architecture_review
    fi

    if [[ "$TASKS_TO_RUN" == *"2"* ]]; then
        task_2_cloudtrail
    fi

    if [[ "$TASKS_TO_RUN" == *"3"* ]]; then
        task_3_config
    fi

    if [[ "$TASKS_TO_RUN" == *"4"* ]]; then
        task_4_guardduty
    fi

    if [[ "$TASKS_TO_RUN" == *"5"* ]]; then
        task_5_vpc_flow_logs
    fi

    if [[ "$TASKS_TO_RUN" == *"6"* ]]; then
        task_6_eventbridge
    fi

    if [[ "$TASKS_TO_RUN" == *"7"* ]]; then
        task_7_verification
    fi

    if [[ "$TASKS_TO_RUN" == *"8"* ]]; then
        task_8_final_signoff
    fi

    # Print completion summary
    echo ""
    log_header "Deployment Complete!"
    echo "Documentation: $DOCS_DIR/"
    echo "Terraform: $TF_DIR/"
    echo ""
    echo "View documentation:"
    echo "  - AGENT-2-TASK-1-ARCHITECTURE-REVIEW.md (architecture details)"
    echo "  - AGENT-2-DEPLOYMENT-SUMMARY.txt (deployment summary)"
    echo ""
}

# Run main
main "$@"
